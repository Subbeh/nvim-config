-- disable auto comments
vim.api.nvim_create_autocmd({ "BufWinEnter" }, {
  callback = function()
    vim.cmd "set formatoptions-=cro"
  end,
})

-- press q to close windows
vim.api.nvim_create_autocmd({ "FileType" }, {
  pattern = {
    "netrw",
    "git",
    "help",
    "man",
    "lspinfo",
    "lir",
    "",
  },
  callback = function()
    vim.cmd [[
      nnoremap <silent> <buffer> q :close<CR>
      set nobuflisted
    ]]
  end,
})

